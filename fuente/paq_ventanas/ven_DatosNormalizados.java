    public void llenarDatos(String tipo,int fila, int columna)
    {
        if (tipo.equals("IN"))
        {
            this.lbl_tipo.setText("Entrada");
            this.txt_valorReal.setText(String.valueOf(ven_MDI.nueva_red.getListaPatrones().getPatron(fila).getValorEntrada(columna)));
            this.lbl_normal.setText(String.valueOf(ven_MDI.nueva_red.getListaPatrones().getPatron(fila).getValorNormalizadoEntrada(columna)));
            this.lbl_maximo.setText(String.valueOf(ven_MDI.nueva_red.getListaPatrones().getMAXIMO()));            //getMaxEntrada(columna));
            this.lbl_minimo.setText(String.valueOf(ven_MDI.nueva_red.getListaPatrones().getMINIMO()));                            //getMinEntrada(columna)));
        }
        else
        {
            this.lbl_tipo.setText("Salida");
            this.txt_valorReal.setText(String.valueOf(ven_MDI.nueva_red.getListaPatrones().getPatron(fila).getValorSalida(columna)));
            this.lbl_normal.setText(String.valueOf(ven_MDI.nueva_red.getListaPatrones().getPatron(fila).getValorNormalizadoSalida(columna)));
            this.lbl_maximo.setText(String.valueOf(ven_MDI.nueva_red.getListaPatrones().getMAXIMO()));                    //getMaxSalida(columna)));
            this.lbl_minimo.setText(String.valueOf(ven_MDI.nueva_red.getListaPatrones().getMINIMO()));                    //getMinSalida(columna)));
         
        }
    }
